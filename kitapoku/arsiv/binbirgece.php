Hükümdar Şehriyar ve Kardeşi Hükümdar
Şahzaman’ın Hikâyesi<br><br>

Doğrusunu Allah bilir ya, anlatıldığına göre çok eski
zamanlarda, Çin Hindi yarımadasında, Sasani hükümdarlarından biri hükümdarlık yapıyordu. Büyük orduları, yardımcıları ve halkı olan bu hükümdarın her biri
bir diğerinden cengâver iki oğlu vardı. Bu iki oğlun, küçüğüne nazaran daha kahraman olan, tebaasını adaletle yönetip halkı tarafından pek sevilen büyüğünün
adı Şehriyar, Semerkandü’l-Acem hükümdarı olan küçüğünün adıysa Şahzaman’dı. Her ikisinin ülkesinde
de her şey yolundaydı, ülkelerini huzur ve refah içinde
yönetiyorlardı. Bu şekilde yirmi yıl geçti. Aradan geçen
bu kadar zamandan sonra kardeşini bir hayli özleyen
Şehriyar, vezirine, kardeşinin ülkesine gidip onu kendisine getirmesi emrini verdi. Vezir aldığı bu emir üzerine hemen yola koyuldu. Küçük kardeşin ülkesine sağ
salim vardı, varır varmaz da huzura çıktı. Hükümdarının selamını kardeşi Şahzaman’a iletti ve Şehriyar’ın
çok özlemiş olduğundan dolayı kardeşinin kendisini
ziyaret etmesini istediğini bildirdi.<br><br>
Şahzaman bunu duyunca hemen hazırlıklara başladı. Vezirini yerine vekil bırakıp çadırları, katırları,
adamları ve yardımcılarıyla birlikte kardeşinin ülkesine doğru yola koyuldu. Ancak gece yarısı olmuştu ki
alması gereken bir şeyi sarayında unuttuğunu hatırlayıp geri döndü. Saraya girdiğinde karısını, kendi yatağında zenci kölelerden biriyle sarmaş dolaş yatarken
gördü. Dünyası karardı. Kendi kendine “Ben daha şehirden ayrılır ayrılmaz bu namussuz kadın bana bunları yapıyorsa, kardeşimin yanında uzunca bir vakit
kaldığımda kim bilir neler yapar?” dedi ve kılıcını çekip ikisini de oracıkta öldürdü. Orada daha fazla oyalanmadan kardeşinin ülkesine doğru yola koyuldu.
Kardeşinin gelişine çok sevinen Şehriyar büyük bir
coşkuyla karşılayıp sevinçle kucakladı onu. Beraberce
oturup hoş sohbetlere daldılar; ancak karısının yaptıklarını hatırlayınca Şahzaman’ın tekrar kederi arttı,
yüzü sarardı, güçten kuvvetten düştü. Kardeşinin bu
hallerini gören Şehriyar, kardeşinin hüznünü ülkesinden ayrılmış olduğuna verip onu kendi haline bıraktı,
hiçbir şey sormadı.
<br><br>
 Şahzaman üzerinden biraz zaman geçince kardeşine içinin acıdığını söyledi, ama sebebini açıklamadı.
Bunun üzerine Şehriyar: “Ava çıkmak istiyordum şu
aralar, sen de gel, açılırsın” dedi. Şahzaman bu öneriyi
kabul etmeyince de tek başına gitti.
Sarayın, Şehriyar’ın bahçelerine bakan pencereleri
vardı. Bir gün Şahzaman bu pencerelerden birinden
etrafı seyrederken sarayın kapısı açıldı, yirmi cariye
ve yirmi köle hükümdarın güzel ve alımlı karısı da aralarında olduğu halde saraydan çıkıp bahçedeki havuzun kenarına geldiler. Elbiselerini çıkarıp hep beraber
oturdular. Hükümdarın karısı: “Hey Mesut!” diye seslenerek siyahi bir köleyi yanına çağırdı. Köle gelince
kadın ona sarıldı, köle de kadına sarılıp orada onunla
ilişkiye girdi. Diğer cariye ve köleler de aynı şeyleri yapıyor, birbirlerine sarılıp öpüşüyorlardı. Bu hal, ta gün
batıncaya kadar böyle devam etti. Hükümdarın kardeşi bütün bunları görünce kendi kendine: “Vallahi
benim başıma gelenler kardeşimin başına gelenlerin
yanında hafif kalırmış. Bu gördüklerim benim başıma
gelenlerden çok daha fena” dedi. Hal böyle olunca kederi azaldı, yemesi içmesi yerine geldi.<br><br>
Bir zaman sonra hükümdar avdan döndü, kardeşiyle kucaklaşıp selamlaştı. Şahzaman’ın renginin değişmiş, yüzüne kan gelmiş olması ve eskiden çok az yiyip
içiyorken şimdi iştahla yiyip içiyor olması Şehriyar’ın
dikkatinden kaçmadı. Şaşkınlıkla: “Yüzünü sararıp solmuş görmüştüm, şimdi ne oldu da rengin yerine geldi,
söyle bana” deyince Şehriyar: “Yüzümün niye sarardığını anlatırım sana, ama rengimin niye yerine geldiği
konusunu anlatamam kusuruma bakma” diye yanıt
verdi. Bunun üzerine Şehriyar: “Tamam, önce renginin
düzelmesinin, durumunun değişmesinin sebebini anlat da bileyim” dedi. Şahzaman: “Ey benim kardeşim,
hani sen vezirini bana gönderip de yanına gelmemi istemiştin ya, ben o zaman hemen hazırlanıp yola koyul-

muştum. Ama daha şehirden yeni çıkmıştım ki aklıma,
sana hediye etmek için yanıma alacağım inciyi sarayda bıraktığım geldi, geri döndüm. Döndüğümde, karımı
kölelerden biriyle benim yatağımda uyurken gördüm,
ikisini de oracıkta öldürdüm. İşte ilk geldiğim zamanlar
bu yaşadıklarımı düşünmekten sararıp solmuş, güçten
kuvvetten kesilmiştim. Rengimin niye yerine geldiği konusundaysa, dediğim gibi beni hoşgör, anlatamam” diyerek başından geçenleri anlattı. Ancak Şehriyar: “Allah
için anlat bana, ne oldu da rengin yerine geldi” şeklinde ısrar edince, gördüklerini kardeşine anlattı ve “Yine
ava çıkıyormuş gibi yap, benim yanımda saklan ki kendi gözlerinle göresin” dedi.<br><br> Hükümdar hemen o dakika
yolculuk emri verdi. Çadırları hazırlayıp askerleri toplayarak yola çıktılar. Şehrin dışına çıkıp çadırları kurdukları vakit hükümdar kölesine kimseyi çadırına sokmaması emrini verdi. Tebdili kıyafet edip gizlice çadırdan
ayrılarak saraya, kardeşinin yanına geldi. Bahçeye bakan pencerenin önüne oturup bir zaman öyle bekledi.
Çok geçmeden cariyeler hanımlarıyla birlikte, yanlarında köleler de olduğu halde geldiler ve Şahzaman’ın
dediği şeyleri yapmaya başladılar. Bu hal ikindiye kadar devam etti. Bütün bunları gören Şehriyarın’ın aklı
başından gitti, kardeşine: “Kalk, kalk hazırlan da kendi
yolumuza gidelim. Bizim yaşadıklarımızı yaşayan bir
kişi daha var mıdır öğrenmedikçe hükümdarlık bizim
neyimize. Böyle yaşamaktansa ölmek evladır” dedi.
<br><br>
Kardeşi, hükümdarın dediklerini kabul etti, sarayın
gizli kapısından çıkarak oradan ayrıldılar. Gece gündüz demeden yol aldılar. Sonunda tuzlu bir denizin
kenarında uzanan bir çayıra vardılar; çayırın orta yerinde bir ağaç, ağacın yanı başında da tatlı bir göze
vardı. Gözeden su içip biraz dinlenmek için oturdular.
Daha bir saat geçmemişti ki deniz birden çalkalanıp

kabardı. Siyah bir duman göğe doğru uzandı, sonra da
kendilerinin olduğu çayıra doğru gelmeye başladı.<br><br>
Korkularından hemen yanı başında bulundukları büyük ağacın tepesine tırmandılar ve olacakları seyretmeye başladılar. Gelen uzun boylu, koca kafalı, geniş
göğüslü bir cindi. Başının üstünde bir sandık taşıyordu. Karaya çıkınca hükümdar ve kardeşinin tepesine
tırmandıkları ağacın yanına gelip gölgesine oturdu.
Sandığı açıp içinden bir kutu çıkardı. Kutuya açınca
içinden güneş gibi göz kamaştıran güzellikte bir kız
çıktı. Kızın güzelliği tıpkı şairin dediği gibiydi:
Karanlıkları deldi günün ışıkları
Nuruyla aydınlattı seherleri
Örtüleri parçalayıp çıktığında ortaya
Secde eder önünde tüm kâinat
Şimşekler onu korumak için çakınca
Gözyaşları yağmurlar gibi dökülür
Cin, kıza bakıp: “Ey zifaf gecesi kaçırdığım ipekler
sultanı, şurada biraz uyumak istiyorum” dedi ve kızın
dizlerine yatıp uyudu.<br><br> Cin uyuduğu vakit kız bir ara başını kaldırdı ve ağacın tepesinde duran hükümdarları

gördü. Dizlerinde uyuyan cinin başını usulca yere koyup kalktı. Ağacın altında dikilip işaretlerle iki kardeşe
inmelerini ve cinden korkmamalarını söyledi. Ancak
Şehriyar ve Şahzaman: “Allah için bizi mazur gör, bırak
da burada kalalım” diye yanıt verdiler. Onlar böyle söyleyince kız: “Eğer inmezseniz vallahi şu cine söylerim
de çok feci bir şekilde öldürür sizi” dedi. Kızın bu sözlerini duyan kardeşler ağaçtan indiler. Kız karşılarına dikilip: “Şimdi, benimle birlikte olun, yoksa sizi cine haber veririm” dedi. Korktuklarından her biri işi ötekine
atmaya başladı. Şehriyar kardeşine: “Haydi kardeşim
istediği şeyi sen yap” dedi. Ancak Şahzaman: “Önce sen
yapmadıkça ben asla yapmayacağım” diye cevap verdi.
Aralarında anlaşamadıklarından her biri bir diğerine,
kaşıyla gözüyle, kızla birlikte olmasını işaret ediyordu.
Kız bunu görünce: “Birbirinizle işaretleşip durduğunuzu görmüyorum sanmayın. Vallahi eğer dediğimi yapmazsanız sizi şimdi cine söyleyeceğim” dedi. Korkularından kızın isteğini yerine getirdiler. <br><br>Kız: “Bekleyin bir
dakika” dedi ve cebinden içinden beş yüz yetmiş adet
yüzük olan bir kese çıkardı. “Bunların hikâyesini biliyor
musunuz?” diye sordu. “Hayır, bilmiyoruz” diye cevap
verdiklerindeyse şöyle dedi: “Bu yüzüklerin sahipleri
cinin haberi yokken benimle birlikte oldular. Şimdi siz
de yüzüklerinizi bana verin.” Hükümdarlar denileni yapıp yüzüklerini verince kız sözlerine şunları ekledi: “Bu
gördüğünüz ifrit beni düğün gecemde kaçırıp bir kutuya koydu, kutuya da bir sandığa koyup sandığa yedi kilit vurdu. Sonra da beni içine koyduğu o sandığı azgın
dalgalı bir denize attı. Ama herkes bilir ki bir kadın bir
şey yapmak istediği zaman hiçbir şey onu o şeyi yapmaktan alıkoyamaz. Ne demiş şair:
İnanma kadınlara
Güvenme verdikleri söze
Ferçlerine bağlıdır rızaları aldatmaları







