Romanın kahramanı Santiago’nun anne ve babası rahip olması için onu papaz okuluna göndermiştir. On altı yaşına geldiğinde rahip olmak istemediğini, okuldan ayrılmayı ve gezginci olmak istediğini babasına söyler. Bunun üzerine babası da, oğluna içinde üç adet altın İspanyol parası olan bir kese vererek oğluna “git, kendine bir sürü al ve en iyi şatonun bizim şatomuz ve en güzel kadınların bizim kadınlarımız olduğunu öğreninceye kadar dünyayı dolaş” der ve oğlunu kutsar. Önce, babasının vermiş olduğu parayla bir koyun sürüsü alır ve yaşamının büyük düşünü gerçekleştirmeye başlar; artık geziyordur.<br><br>

Akşam yattığında uykusunda gördüğü rüyaların da etkisinde kalarak; gördüğü bir düşün gerçekleşme olasılığının yaşamını ilginçleştireceğini düşünür ve o şekilde hareket eder. Romanın ana konusunu teşkil eden Mısır Piramitleri’ne gitmesi ve orada hazine bulacağı ona rüyasında söylenir. Romanın kahramanı, rüyasını gerçekleştirmek için önce bir falcı kadına rüyasını anlatır. Falcı kadın Salem kralı olarak tanıtan yaşlı adamla konuşur, kendi amaçlarını anlatır. Yaşlı adam, hayatın gizemleri hakkındaki bilgiye karşılık Santiago’dan sürüsünün onda birini vermesini ister. Yaşlı adam, Santiago’ya biri beyaz diğeri siyah olmak üzere iki adet gizemli taş verir ve siyah olanı “evet”, beyaz olanı “hayır” anlamını taşıyan bu taşları “zora düştüğün zamanlarda kullanırsın ancak kendi kararını kendin vermeye çalış” der.<br><br>

Mısır’a gitmek için önce koyun sürüsünü satar ve parasını cebine koyarak yola çıkar. Arap çocuğu ile tanışır, beraber pazara giderler. Fakat Arap paralarla birlikte kaçarak Santiago’yu bu şehirde parasız pulsuz bırakır. Bunun üzerine Santiago para kazanmak için bir billuriyeci dükkanında çalışmaya başlar. 6 ay kadar burada çalıştıktan sonra Santiago yeterli parayı kazanarak tekrar yola koyulur. Yolda bir İngiliz’le karşılaşır. Yolda karşılaştıkları güçlüklerde kendi kişisel menkıbelerini aramak üzere yola çıktıklarını söylerler.<br><br>

Santiago, yüreğinin söylediklerini dikkatle dinleyerek çölde ilerlemesine devam eder. Karşılaştıkları güçlükler karşısında hep kendi kişisel menkıbesine güvenir ve sonunda kumullar tepesine ulaşır. Piramitler, bütün görkemiyle karşısında yükseliyordur. “Gerçekte kendi kişisel menkıbesini yaşayan kimseye karşı hayat cömerttir” diye düşünür. Sabah uyandığında gerçekten bulunduğu yeri kazmış ve içi mücevher dolu bir sandık bularak rüyasında gördüğü ve Mısır’a piramitlere kadar gidip bulmayı arzuladığı hazineye kavuşmuştur..






